    <div class="section">
      <h3>Preparação do Substrato</h3>
      <p>Antes do plantio, é essencial preparar o substrato corretamente.</p>
      <div class="card">
        <ol>
          <li class="card-item">Mistura dos Materiais: Combine 20% de areia grossa, 40% de terra vegetal peneirada e
            40% de substrato. Certifique-se de que a mistura esteja levemente úmida, sem estar encharcada.</li>
          <li class="card-item">Preparação do Vaso: No fundo do vaso, coloque uma camada de pedra utilizada na
            construção civil para tampar completamente os buracos do vaso.</li>
        </ol>
      </div>
    </div>

    <div class="section">
      <h3>Plantio e Cuidados Iniciais</h3>
      <p>Ao plantar a Rosa do Deserto, siga estes passos:</p>
      <div class="card">
        <ul class="card1">
          <li class="card-item">Levantar a Planta: Durante o plantio, é possível levantar a planta, deixando uma parte
            do caudex à mostra.</li>
          <li class="card-item">Rega Inicial: Mantenha a planta em local sombreado por uma semana após o plantio. Após
            esse período, regue o substrato levemente.</li>
          <li class="card-item">Gradual Exposição ao Sol: Após três semanas, inicie a exposição gradual da planta ao
            sol pela manhã, aumentando o tempo diário de exposição gradualmente.</li>
          <li class="card-item">Cuidados Especiais: Proteja o caudex com sombrite nos primeiros dois meses de
            exposição ao sol.</li>
        </ul>
      </div>
    </div>

    <div class="section">
      <h3>Cuidados Contínuos</h3>
      <div class="card">
        <ul class="card2">
          <li class="card-item">Verificação de Umidade: Pressione o caudex da Rosa do Deserto para determinar a
            necessidade de rega. Evite regar em excesso para evitar a podridão do caudex.</li>
          <li class="card-item">Drenagem Adequada: Evite o acúmulo de água no fundo do vaso utilizando blocos de
            construção com os furos voltados para cima para auxiliar na drenagem.</li>
          <li class="card-item">Prevenção de Doenças: Mantenha um bom controle de pragas e nutrientes para garantir o
            desenvolvimento saudável da planta.</li>
        </ul>
      </div>
    </div>

    <div class="section">
      <h2>Conclusão</h2>
      <p>A paciência é fundamental para o cultivo bem-sucedido da Rosa do Deserto. Lembre-se de que o excesso de água
        é prejudicial à planta, e permita que a natureza siga seu curso gradualmente. Estamos comprometidos em
        fornecer suporte e esclarecimento para garantir o sucesso do seu cultivo.</p>
